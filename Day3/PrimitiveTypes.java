package first_session;

//A Java Program to Check The default Values of all the Primitive types in java.
public class PrimitiveTypes {
	static int intVariable;
	static float floatVariable;
	static String stringVariable;
	static double doubleVariable;
	static char characterVariable;
	static byte byteVariable;
	static long longVariable;
	static short shortVariable;
	
	public static void main(String[] args) {
		System.out.println("The Default Value of int is    : "+intVariable);
		System.out.println("The Default Value of float is  : "+floatVariable);
		System.out.println("The Default Value of String is : "+stringVariable);
		System.out.println("The Default Value of double is : "+doubleVariable);
		System.out.println("The Default Value of char is   : "+characterVariable);
		System.out.println("The Default Value of byte is   : "+byteVariable);
		System.out.println("The Default Value of long is   : "+longVariable);
		System.out.println("The Default Value of short is  : "+shortVariable);
	}

}
